    private void populateTable() {
            DefaultTableModel model= (DefaultTableModel) SupplierTable.getModel();
            model.setRowCount(0);
            for(Supplier obj : suppData.getSupplierDataFromDb()){
                Object[] row= new Object[3];
                row[0] = obj;
                row[1] = obj.getId();
                row[2] = obj.getCity();
                model.addRow(row);
            }

    }
    
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex= SupplierTable.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to update/ delete");
        }
        DefaultTableModel model= (DefaultTableModel) SupplierTable.getModel();
        Supplier supp = (Supplier) model.getValueAt(selectedRowIndex, 0);
        suppData.deleteSupplier(supp);
        JOptionPane.showMessageDialog(this, "Selected row deleted");
        populateTable();
        
        txtHName.setText("");
        txtID.setText("");
        txtAddress.setText("");
        txtCity.setText("");
        txtState.setText("");
        txtAreaCode.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed

