    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        ArrayList<Supplier> SupplierData1 = new ArrayList();
        try {
            Connection con=SQLConnection.dbconnector();
            String search = txtSearchUser.getText();
            String sql= String.format("select * from SupplierData where name like \"%s\" or id like \"%s\" or city like \"%s\";", search, search, search);
            PreparedStatement ps=con.prepareStatement(sql);
            ResultSet st=ps.executeQuery();
            
             while(st.next())
             {
                 Supplier p=new Supplier();               
                 p.setId(st.getString("id"));                
                 p.setCity(st.getString("city"));
                 p.setAddress(st.getString("address"));
                 p.setName(st.getString("name"));
                 p.setState(st.getString("state"));
                 p.setAreacode(st.getString("areacode"));
                
                 SupplierData1.add(p);
           
             }
             
            DefaultTableModel model= (DefaultTableModel) SupplierTable.getModel();
            model.setRowCount(0);
            for(Supplier obj : SupplierData1){
                Object[] row= new Object[3];
                row[0] = obj;
                row[1] = obj.getId();
                row[2] = obj.getCity();

                model.addRow(row);
            }
             
        } catch (SQLException ex) {
            System.out.print(ex.getMessage());
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

