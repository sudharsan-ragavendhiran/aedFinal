    private void populateTable() {
            DefaultTableModel model= (DefaultTableModel) DoctorTable.getModel();
            model.setRowCount(0);
            for(Doctor obj : docData.getDoctorDataFromDb()){
                Object[] row= new Object[4];
                row[0] = obj;
                row[1] = obj.getSpecialisation();
                row[2] = obj.getGender();
                model.addRow(row);
            }

    }

