    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex= DoctorTable.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to update/ delete");
        }
        DefaultTableModel model= (DefaultTableModel) DoctorTable.getModel();
        Doctor doc = (Doctor) model.getValueAt(selectedRowIndex, 0);
        docData.deleteDoctor(doc);
        JOptionPane.showMessageDialog(this, "Selected row deleted");
        populateTable();
        txtUsername.setText("");
        txtName.setText("");
        txtSpecialisation.setText("");
        txtEmail.setText("");
        txtDOB.setText("");
        txtGender.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed

