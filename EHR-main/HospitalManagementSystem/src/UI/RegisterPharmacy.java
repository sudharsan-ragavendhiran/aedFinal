    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String pharmacyName = txtHName.getText();
        String pharmacyId = txtID.getText();
        String pharmacyAddress = txtAddress.getText();
        String pharmacyCity = txtCity.getText();
        String pharmacyState = txtState.getText();
        String pharmacyAreaCode = txtAreaCode.getText();
        
        if(pharmacyName.equals("") || pharmacyId.equals("") || pharmacyAddress.equals("") || pharmacyCity.equals("") || pharmacyState.equals("") || pharmacyAreaCode.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter details in all the fields.");
        }
        
        pharmacy ph = new pharmacy();
        
        ph.setName(pharmacyName);
        ph.setCity(pharmacyCity);
        ph.setId(pharmacyId);
        
        
        pharmacyData phData = new pharmacyData();
        phData.createPharmacyInDb(ph);
        
        JOptionPane.showMessageDialog(this, "Created Successfully.");
        
        txtHName.setText("");
        txtID.setText("");
        txtAddress.setText("");
        txtCity.setText("");
        txtState.setText("");
        txtAreaCode.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed


