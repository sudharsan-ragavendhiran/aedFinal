    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex= DoctorTable.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a doctor to book appointment");
        }
        DefaultTableModel model= (DefaultTableModel) DoctorTable.getModel();
        Doctor ia = (Doctor) model.getValueAt(selectedRowIndex, 0);
//      iad.deleteInsAgency(ia);
        Encounter enc = encData.addNewEncounter();
        
        enc.setDoctorUsername(ia.getUsername());
        enc.setPatientUsername(username);
        enc.setType("appointment");
        enc.setBp("");
        enc.setComments("");
        enc.setPulse("");
        enc.setTemp("");
        enc.setDoctorName(ia.getName());
        enc.setHospital(ia.getHospitalName());
        encData.createEncounInDb(enc);
        JOptionPane.showMessageDialog(this, "Appointment booked");

        
    }//GEN-LAST:event_btnViewActionPerformed
private void populateTable() {
            DefaultTableModel model= (DefaultTableModel) DoctorTable.getModel();
            model.setRowCount(0);
            for(Doctor obj : docData.getDoctorInfoFromDb()){
                Object[] row= new Object[3];
                row[0] = obj;
                row[1] = obj.getSpecialisation();
                row[2] = obj.getHospitalName();
                model.addRow(row);
            }
    }


