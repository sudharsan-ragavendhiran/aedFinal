    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        //UserData uData = new UserInfo();
        String amt = jTextField2.getText();
        String pat = username; //jComboBox2.getSelectedItem().toString();
        String ins = jComboBox1.getSelectedItem().toString();
        int insId = 0;
        try {
            Connection con=SQLConnection.dbconnector();
            String sql="select * from InsuranceAgencyData where name= '"+ins+"';";
            PreparedStatement ps=con.prepareStatement(sql);
            ResultSet st=ps.executeQuery();

            while(st.next()){
                insId = st.getInt("id");
                break;

            }
            InsurancePolicyInfo x = new InsurancePolicyInfo();
            InsurancePolicy y = x.addNewInsurPolicy();
            y.setPatientUsername(pat);
            y.setInsuranceAgencyId(String.valueOf(insId));
            x.createInsurPolicyInDb(y);
            jTextField2.setText("");
        }catch (SQLException ex) {
            //Logger.getLogger(DoctorDirectory.class.getName()).log(Level.SEVERE, null, ex);
            System.out.print(ex.getMessage());
        }

    }//GEN-LAST:event_btnUpdateActionPerformed


