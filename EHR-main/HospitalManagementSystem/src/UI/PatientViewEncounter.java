    private void populateTable() {
        DefaultTableModel model= (DefaultTableModel) EncounterTable.getModel();
        model.setRowCount(0);
        encData = new EncounterData();
        //JOptionPane.showMessageDialog(this, username);
        for(Encounter obj : encData.getEncounterDataFromDbForPatient(username, "encounter")){
            Object[] row= new Object[4];
            row[0] = obj;
            row[1] = obj.getDoctorName();
            row[2] = "";
            row[3] = "";
            model.addRow(row);
        }
    }

