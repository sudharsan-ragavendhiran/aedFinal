    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex= EncounterTable.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to view!");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) EncounterTable.getModel();
        Encounter encp= (Encounter) model.getValueAt(selectedRowIndex,0);
        
        txtDName.setText(encp.getDoctorName());
        txtHospName.setText(encp.getHospital());
        txtSpecialsation.setText(encp.getComments());
        //txtDate.setText("");
        //txtTime.setText("");
        txtBP.setText(encp.getBp());
        txtTemp.setText(encp.getTemp());
        txtPulse.setText(encp.getPulse());
        
    }//GEN-LAST:event_btnViewActionPerformed

