    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String firmName = txtName.getText();
        String firmID = txtID.getText();
        String firmAddress = txtAddress.getText();
        String firmCity = txtCity.getText();
        String firmState = txtState.getText();
        String firmAreacode = txtAreaCode.getText();
        
        if(firmName.equals("") || firmID.equals("") || firmAddress.equals("") || firmCity.equals("") || firmState.equals("") || firmAreacode.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter details in all the fields.");
            return;
        }
        
        InsuranceAgency InsAgency = new InsuranceAgency();
        
        InsAgency.setName(firmName);
        InsAgency.setId(firmID);
        InsAgency.setAddress(firmAddress);
        InsAgency.setCity(firmCity);
        InsAgency.setState(firmState);
        InsAgency.setAreacode(firmAreacode);
        
        InsuranceAgencyData InsAgencyData = new InsuranceAgencyData();
        InsAgencyData.createInsuranceAgencyInDb(InsAgency);
        
        JOptionPane.showMessageDialog(this, "Created Successfully.");
        
        txtName.setText("");
        txtID.setText("");
        txtAddress.setText("");
        txtCity.setText("");
        txtState.setText("");
        txtAreaCode.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed


