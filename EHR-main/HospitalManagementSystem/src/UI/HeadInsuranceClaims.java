    private void populateTable() {
            DefaultTableModel model= (DefaultTableModel) InsuranceTable.getModel();
            model.setRowCount(0);
            for(InsuranceClaims obj : icD.getPlacedInsuranceRequestDataFromDb()){
                Object[] row= new Object[3];
                row[0] = obj;
                row[1] = obj.getId();
                row[2] = obj.getAmount();
                model.addRow(row);
            }

    }
    
    
