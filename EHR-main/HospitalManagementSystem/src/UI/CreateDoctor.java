    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String docUsername = txtUsername.getText();
        String docName = txtName.getText();
        String docEmail = txtEmail.getText();
        String docPhone = txtPhone.getText();
        String docSpecialisation = txtSpecialisation.getText();
        String docDob = txtDOB.getText();
        String docGender = txtGender.getText();
        
        if(docUsername.equals("") || docName.equals("") || docEmail.equals("") || docPhone.equals("") || docSpecialisation.equals("") || docDob.equals("") || docGender.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter details in all the fields.");
            return;
        }
        
        Doctor doc = new Doctor();
        
        doc.setUsername(docUsername);
        doc.setName(docName);
        doc.setDob(docDob);
        doc.setGender(docGender);
        doc.setEmail(docEmail);
        doc.setPhoneNumber(docPhone);
        doc.setSpecialisation(docSpecialisation);
        doc.setHospitalName(docData.searchHospitalNameInDb(username));
        
        docData.createDoctorInDb(doc);
       UserData userData = new UserData();
        User p = userData.addNewUser();
            p.setName(docName);
            p.setUsername(docUsername);
            p.setRole("doctor");
            p.setOrg("hospital");
            p.setPassword(txtUsername1.getText());
            p.setOrgName(docData.searchHospitalNameInDb(username));
            boolean res = userData.createUserInDb(p);
            if(!res){
                JOptionPane.showMessageDialog(this,"retry" , "warning", HEIGHT);
            }
            JOptionPane.showMessageDialog(this,"User created");
        
        //JOptionPane.showMessageDialog(this, "Created Successfully.");
        
        txtUsername.setText("");
        txtName.setText("");
        txtSpecialisation.setText("");
        txtPhone.setText("");
        txtDOB.setText("");
        txtGender.setText("");
        txtEmail.setText("");
        txtUsername1.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

