    private void populateTable() {
            DefaultTableModel model= (DefaultTableModel) PatientUsernameTable.getModel();
            model.setRowCount(0);
            for(Encounter obj : ed.getEncounterDataFromDb()){
                Object[] row= new Object[4];
                row[0] = obj;
                row[1] = obj.getPatientUsername();
                model.addRow(row);
            }

    }
    
    
    

