    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex= PatientUsernameTable.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to view!");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) PatientUsernameTable.getModel();
        Encounter en= (Encounter) model.getValueAt(selectedRowIndex,0);
        
        
        txtPusername.setText(en.getPatientUsername());
        txtBP.setText(en.getBp());
        txtTemp.setText(en.getTemp());
        txtPulse.setText(en.getPulse());
        txtDoc.setText(en.getDoctorUsername());
    }//GEN-LAST:event_btnViewActionPerformed

